function splice(array, start, deleteCount, ...args) {
  start = Math.min(array.length, start);
  deleteCount = Math.min(array.length - start, deleteCount)

  let deletedElements = slice(array, start, start + deleteCount);
  let addCount = args.length;
  let secondSplice = slice(array, start + deleteCount, array.length)

  array.length = start
  array.push(...args, ...secondSplice)

  return deleteCount === 0 ?  [] : deletedElements;
}

console.log(splice([1, 2, 3], 1, 2));              // [2, 3]
