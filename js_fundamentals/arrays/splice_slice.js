function slice(array, begin, end) {
  begin = Math.min(array.length, begin);
  end = Math.min(array.length, end);

  let slicedArray = [];
  for (let index = begin; index < end; index += 1) {
    slicedArray.push(array[index]);
  }

  return slicedArray;
}

